
from datetime import date, datetime, time

#date formata no formato de data
data = date(2025, 5,15)
print(data)

#date.today retorna a data de hoje (data atual)
print(date.today())

#datetime retorna a data e hora
data_hora = datetime(2025, 5, 15, 21, 18, 30)
print(data_hora)

#datetime.today retorna a data e hora(atual)
data_hora = datetime(2025, 5, 15, 21, 18, 30)
print(data_hora)
print(datetime.today())
