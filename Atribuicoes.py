#atribuição padrão
saldo = 10
print(saldo)

#somando atribuição
saldo += 200
print(saldo)

#subtraindo atribuição
saldo -= 100
print(saldo)

#multiplicando atribuição
saldo *= 10
print(saldo)

#dividindo por numero inteiro atribuição
saldo //= 2
print(saldo)

#dividindo atribuição
saldo /= 2
print(saldo)

#usando a potencia na atribuição
saldo **=0
print(saldo)
